import React from "react";
import Square from "./Square";
import "./Tutorial.css";

interface Props {
  squares: string[];
  onClick: (i: number) => void;
}

interface States {}

class Board extends React.Component<Props, States> {
  // イベントを表す props には on[Event] という名前、イベントを処理するメソッドには handle[Event]
  renderSquare(i: number) {
    return (
      <Square
        value={this.props.squares[i]}
        onClick={() => this.props.onClick(i)}
      />
    );
  }
  render() {
    return (
      <div>
        <div className="board-row">
          {this.renderSquare(0)}
          {this.renderSquare(1)}
          {this.renderSquare(2)}
        </div>
        <div className="board-row">
          {this.renderSquare(3)}
          {this.renderSquare(4)}
          {this.renderSquare(5)}
        </div>
        <div className="board-row">
          {this.renderSquare(6)}
          {this.renderSquare(7)}
          {this.renderSquare(8)}
        </div>
      </div>
    );
  }
}

export default Board;
